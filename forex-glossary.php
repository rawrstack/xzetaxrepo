<h3>U</h3>
<p>
<strong>
<span class="glossary">Unconvertible Currency:</span>
</strong>
A currency that cannot be exchanged for another because of foreign exchange regulations.
</p>
<p>
<strong>
<span class="glossary">Unit:</span>
</strong>
A widely used quantity of currency. In FXTrade, one unit of USD is equal to one United States dollar, while one unit of EUR is one Euro. For JPY, one unit is equivalent to one Yen. One unit is the smallest trade size in FXTrade.
</p>
