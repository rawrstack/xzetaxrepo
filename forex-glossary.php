<h3>B</h3>
<p>
<span class="glossary" style="font-weight: bold">Back Office:</span>
The office location, or department, where the processing of financial transactions takes place.
</p>
<p>
<span class="glossary" style="font-weight: bold">Band:</span>
In countries where the currency is pegged, the range in which the rates are permitted to fluctuate.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bank Rate: </span>
The rate at which a central bank is prepared to lend money to its domestic banking system.
</p>
<p>
<span class="glossary" style="font-weight: bold">Banking Day:</span>
Days of the week when commercial banks are open for business in the country of the particular currency traded.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bar Charts:</span>
A popular format for studying the price action of currency pairs.
</p>
<p>
<span class="glossary" style="font-weight: bold">Base Currency:</span>
In terms of foreign exchange trading, currencies are quoted in terms of a currency pair. The first currency in the pair is the base currency. The base currency is the currency against which exchange rates are generally quoted in a given country. Examples: USD/JPY, the US Dollar is the base currency; EUR/USD, the Euro is the base currency.
</p>
<p>
<span class="glossary" style="font-weight: bold">Basis Point:</span>
One hundredth of one percent, or 0.0001.
</p>
<p>
<span class="glossary" style="font-weight: bold">Basket of USD Short:</span>
A number of operations where the USD is being sold against various currencies.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bear:</span>
A trader who believes prices will fall.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bear Market:</span>
An extended period of general price decline in an individual security, an asset, or a market.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bid:</span>
The price at which an investor can place an order to buy a currency pair; the quoted price where an investor can sell a currency pair. This is also known as the 'bid price' and 'bid rate'.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bid/Ask Spread:</span>
The point difference between the bid and offer (ask) price.
</p>
<p>
<span class="glossary" style="font-weight: bold">Big Figure:</span>
The first two or three digits of a foreign exchange price or rate. Examples: USD/JPY rate of 108.05/10 the big figure is 108. EUR/USD price of .8325/28 the big figure is .83
</p>
<p>
<span class="glossary" style="font-weight: bold">Book:</span>
The total number of currency positions a dealer has at any given moment. Typically, the dealer aims to have a net position of zero in terms of risk. This means that for the aggregate, all customers’ long and short positions balance each other out.
</p>
<p>
<span class="glossary" style="font-weight: bold">Broker:</span>
An agent, who executes orders to buy and sell currencies and related instruments either for a commission or on a spread. Brokers are agents working on commission and not principals or agents acting on their own account. In the foreign exchange market brokers tend to act as intermediaries between banks, bringing buyers and sellers together for a commission paid by the initiator or by both parties. There are four or five major global brokers operating through subsidiaries, affiliates and partners in many countries.
</p>
<p>
<span class="glossary" style="font-weight: bold">Brokerage:</span>
A company that offers trading services to the public.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bull:</span>
Trader who believes that prices will rise.
</p>
<p>
<span class="glossary" style="font-weight: bold">Bull Market:</span>
A market that is on a consistent upward trend.
</p>
<p>
<span class="glossary" style="font-weight: bold">Buy Limit Order:</span>
An order to execute a transaction at a specified price (the limit) or lower.
</p>
<p>
<span class="glossary" style="font-weight: bold">Buy on Margin:</span>
The process of buying a currency pair where a client pays cash for part of the overall value of the position. The word margin refers to the portion the investor puts up rather than the portion that is borrowed.
</p>
