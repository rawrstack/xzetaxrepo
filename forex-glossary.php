<h3>E</h3>
<p>
<span class="glossary" style="font-weight: bold">Easing:</span>
Refers to either a small price decline in a currency or when a central bank engages in monetary policy to spur spending. An example of central bank easing would be lowering of interest rates.
</p>
<p>
<span class="glossary" style="font-weight: bold">ECN Broker:</span>
Forex ECN brokers provide access to an electronic trading network, supplied with streaming quotes from the top tier banks in the world. By trading through an ECN broker, a currency trader generally benefits from greater price transparency, faster processing, increased liquidity and more availability in the marketplace.
</p>
<p>
<span class="glossary" style="font-weight: bold">Economic Indicator:</span>
A statistic that is used to gauge current economic conditions.
</p>
<p>
<span class="glossary" style="font-weight: bold">Effective Exchange Rate:</span>
Explanation of a country's currency strength or weakness entirely on its trade balance.
</p>
<p>
<span class="glossary" style="font-weight: bold">Electronic Communication Network:</span>
An electronic communication network (ECN) is the term used in financial circles for a type of computer system that facilitates trading of financial products outside of stock exchanges. The primary products that are traded on ECNs are stocks and currencies. FX ECN brokers provide access to an electronic trading network, supplied with streaming quotes from the top tier banks in the world. By trading through an ECN broker, a currency trader generally benefits from greater price transparency, faster processing, increased liquidity and more availability in the marketplace.
</p>
<p>
<span class="glossary" style="font-weight: bold">Elliot Wave Principle:</span>
An attempt to explain market activity by ascribing a pattern of eight waves to any complete cycle. The eight wave patterns consist of a five-stage advance and a three-stage correction.
</p>
<p>
<span class="glossary" style="font-weight: bold">End of Day Mark to Market:</span>
The value of all open positions in a dealer's book based on the closing market rates. In addition, any profits or losses are recorded.
</p>
<p>
<span class="glossary" style="font-weight: bold">Entity Trading Account:</span>
A trading account that does not belong to an individual, but rather to a company that has designated a person to be responsible for it’s trading decisions.
</p>
<p>
<span class="glossary" style="font-weight: bold">Equities:</span>
Ownership interest in a corporation in the form of common stock or preferred stock.
</p>
<p>
<span class="glossary" style="font-weight: bold">Equity:</span>
Total assets minus total liabilities; also called net worth.
</p>
<p>
<span class="glossary" style="font-weight: bold">Equity Curve:</span>
The value of a trading account graphed over a period of time.
</p>
<p>
<span class="glossary" style="font-weight: bold">Escrow Account:</span>
A segregated account where customer money is kept separate from a dealer's operating funds.
</p>
<p>
<span class="glossary" style="font-weight: bold">Euro Interbank Offered Rate:</span>
The Euro Interbank Offered Rate or Euribor is a daily reference rate based on the averaged interest rates at which banks offer to lend unsecured funds to other banks in the Euro wholesale money market or interbank market.
</p>
<p>
<span class="glossary" style="font-weight: bold">Eurocurrency:</span>
A currency that is deposited in a financial institution located outside the currency's country of origin.
</p>
<p>
<span class="glossary" style="font-weight: bold">Eurodollar:</span>
US Dollars deposited in a bank outside the USA.
</p>
<p>
<span class="glossary" style="font-weight: bold">European Monetary System:</span>
An arrangement in the 1970s and 1980s where many European countries linked their currencies to prevent large fluctuations in value. It was one of several initiatives leading to the deployment of the Euro.
</p>
<p>
<span class="glossary" style="font-weight: bold">Excess Margin Deposits:</span>
Deposited funds in a trading account above and beyond what is required for margin requirements.
</p>
<p>
<span class="glossary" style="font-weight: bold">Exchange:</span>
The physical location of trading activity. Some famous examples include the New York Stock Exchange or the Chicago Mercantile Exchange.
</p>
<p>
<span class="glossary" style="font-weight: bold">Exchange Control:</span>
Various devices a central bank uses for controlling the movement of foreign exchange so as to not deplete a country's reserves.
</p>
<p>
<span class="glossary" style="font-weight: bold">Exchange Rate:</span>
In finance, the exchange rates (also known as the foreign-exchange rate, Forex rate or FX rate) between two currencies specifies how much one currency is worth in terms of the other. For example an exchange rate of 102 Japanese Yen (JPY, ¥) to the United States dollar (USD, $) means that JPY 102 is worth the same as USD 1.
</p>
<p>
<span class="glossary" style="font-weight: bold">Execution:</span>
Completing a trade.
</p>
<p>
<span class="glossary" style="font-weight: bold">Exit:</span>
In the case of a long position, the sale of the long currency. In a short position, the purchase of the short currency, resulting in a closed position.
</p>
<p>
<span class="glossary" style="font-weight: bold">Exotic:</span>
As opposed to the major currencies, which are heavily traded, exotics are the less traded currencies.
</p>
