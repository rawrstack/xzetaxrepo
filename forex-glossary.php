<h3>C</h3>
<p>
<span class="glossary" style="font-weight: bold">Cable:</span>
Also known as Sterling. Dealer slang for the GPB/USD currency pair.
</p>
<p>
<span class="glossary" style="font-weight: bold">Carry Currencies:</span>
High interest rate currencies.
</p>
<p>
<span class="glossary" style="font-weight: bold">Carry Grid:</span>
A grid of positions (including open orders, take profits, and stop losses) built on a carry trading strategy.
</p>
<p>
<span class="glossary" style="font-weight: bold">Carry Positive:</span>
A carry trade where you are long the high interest currency and short the low interest currency. Excluding the volatility of the currency pair, this
<a href="http://www.vantagefx.com/why-vantage-fx/forex-strategy-trader/">forex strategy</a>
is profitable based on the interest rate differential between the two countries.
</p>
<p>
<span class="glossary" style="font-weight: bold">Carry Trade:</span>
The carry is the cost of keeping a position open overnight. Each currency has a different interest rate associated with it. You are paid interest on the currency you are long on, and you must pay interest on the currency on which you are short. The difference is the carry, sometimes referred to as the cost of carry.
</p>
<p>
<span class="glossary" style="font-weight: bold">Cash on Deposit:</span>
Funds deposited in a trading account.
</p>
<p>
<span class="glossary" style="font-weight: bold">Chartist:</span>
A person who attempts to predict prices by analysing past price movements as recorded on a chart.
</p>
<p>
<span class="glossary" style="font-weight: bold">Closed Position:</span>
A transaction that offsets the number of units in a previous open position. In the case of a long position, selling the exact number of units so that your exposure in the market is zero.
</p>
<p>
<span class="glossary" style="font-weight: bold">Closing a Position:</span>
The process of selling or buying a foreign exchange position resulting in the liquidation (squaring up) of the position.
</p>
<p>
<span class="glossary" style="font-weight: bold">Closing Market Rate:</span>
The rate at which a position can be closed based on the market price at end of the day.
</p>
<p>
<span class="glossary" style="font-weight: bold">Commission:</span>
The fee that a broker may charge clients for dealing on their behalf.
</p>
<p>
<strong>
<span class="glossary">Confirmation:</span>
</strong>
Written acknowledgment of a trade, listing important details such as the date, the size of the transaction, the price, the commission, and the amount of money involved.
</p>
<p>
<span class="glossary" style="font-weight: bold">Consumer Price Index:</span>
A month-to-month economic indicator, which gauges changes in the cost of living by measuring price changes in a common basket of goods and services that most people use, such as food, clothing, transportation, and entertainment.
</p>
<p>
<span class="glossary" style="font-weight: bold">Conversion Rate:</span>
The value of one currency exchanged for another currency.
</p>
<p>
<span class="glossary" style="font-weight: bold">Copey:</span>
Traders' term for the Danish Krone.
</p>
<p>
<span class="glossary" style="font-weight: bold">Correlation:</span>
A statistical term that refers to a relationship between two seemingly independent things. In Forex for example, one could argue that the Euro and the Sterling have a higher correlation than, for example, the Euro and the Brazilian Real.
</p>
<p>
<span class="glossary" style="font-weight: bold">Counterpart:</span>
A participant in a financial transaction.
</p>
<p>
<span class="glossary" style="font-weight: bold">Counterparty:</span>
The other party in a Forex deal. In online spot Forex, the counterparty is the market maker.
</p>
<p>
<span class="glossary" style="font-weight: bold">Country Risk:</span>
By virtue of economic, political, and geographical factors, some countries are more stable than others. Country risk in reference to Forex means the stability of the currency and the creditworthiness of its bonds.
</p>
<p>
<span class="glossary" style="font-weight: bold">Cover:</span>
(1) To take out a forward foreign exchange contract.
<br>
          (2) To close out a short position by buying currency or securities which have been sold.
</p>
<p>
<span class="glossary" style="font-weight: bold">Cross Rate:</span>
The exchange rate between 2 currencies where neither of the currencies are USD.
</p>
<p>
<span class="glossary" style="font-weight: bold">Currency Code:</span>
The currency codes are specified by ISO 4217. Most codes are composed of the country's two-character country code, and the first character of the national currency name.
</p>
<p>
<span class="glossary" style="font-weight: bold">Currency:</span>
Money issued by a government. Coins and paper money. It is a form of money used as a unit of exchange within a country.
</p>
<p>
<span class="glossary" style="font-weight: bold">Currency Pair:</span>
The two currencies in a foreign exchange transaction. The “EUR/USD” is an example of a currency pair.
</p>
<p>
<span class="glossary" style="font-weight: bold">Currency Risk:</span>
The risk that shifts in foreign exchange rates may undermine the dollar or any other foreign currency value of overseas investments.
</p>
