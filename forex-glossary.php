<h3>A</h3>
<p>
<span class="glossary" style="font-weight: bold">Adjustable Peg:</span>
Exchange rate regimen where a currency's exchange rate is pegged (fixed) in relation to a stronger currency, such as the US Dollar or the Euro. The pegged rate is adjusted occasionally in an attempt to improve the country's competitive position. For example, China's Yuan is sometimes pegged to the US Dollar.
</p>
<p>
<span class="glossary" style="font-weight: bold">Aggregated Risk:</span>
A bank’s exposure to Forex contracts from a single customer.
</p>
<p>
<span class="glossary" style="font-weight: bold">Agio:</span>
A fee charged to exchange money from one currency to another.
</p>
<p>
<span class="glossary" style="font-weight: bold">Ask:</span>
The price at which sellers are willing to sell a currency pair, also known as the 'offer', 'ask price', and 'ask rate'.
</p>
<p>
<span class="glossary" style="font-weight: bold">Asset Class:</span>
An item that has value; an investment such as stocks, options, or Forex.
</p>
<p>
<span class="glossary" style="font-weight: bold">AUD/USD:</span>
The abbreviation for the Australian dollar and U.S. dollar (AUD/USD) currency pair or cross. The currency pair tells the reader how many U.S. dollars (the quote currency) are needed to purchase one Australian dollar (the base currency)
</p>
<p>
<span class="glossary" style="font-weight: bold">Aussie:</span>
Dealer slang for the AUD/USD currency pair.
</p>
<p>
<span class="glossary" style="font-weight: bold">Authorised Dealer:</span>
Depending on the regulatory body, a dealer authorised to deal in Forex.
</p>
<p>
<span class="glossary" style="font-weight: bold">Automated Dealer:</span>
A trader who uses an automated system to input trades without any human input.
</p>
