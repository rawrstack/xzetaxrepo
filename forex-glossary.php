<span class="glossary">Regulated Market:</span>
</strong>
A market in which a government agency monitors and regulates industry activity to protect investors. An example is Forex trading in the United States.
</p>
<p>
<strong>
<span class="glossary">Resistance:</span>
</strong>
Price level at which technical analysts note persistent selling of a currency.
</p>
<p>
<strong>
<span class="glossary">Retail FX Market:</span>
</strong>
Comprises a wide range of non-institutional traders, from large organizations to individual investors. In less than 10 years, a relatively small number of online currency brokers and market makers have had a massive effect on this market by efficiently exploiting technology, driving a five-fold decrease in the cost of trading.
</p>
<p>
<strong>
<span class="glossary">Revaluation:</span>
</strong>
Daily calculation of potential profits or losses on open positions based on the difference between the settlement price of the previous trading day and the current trading day.
</p>
<p>
<strong>
<span class="glossary">Right Hand Side:</span>
</strong>
Refers to the ask or offer price. This is the price at which traders buy.
</p>
<p>
<strong>
<span class="glossary">Risk (Foreign Exchange Risk):</span>
</strong>
The risk that the exchange rate on a foreign currency will move against the position held by an investor such that the value of the investment is reduced.
</p>
<p>
<strong>
<span class="glossary">Risk Capital:</span>
</strong>
The amount of money one could risk without impinging on one's accustomed lifestyle.
</p>
<p>
<strong>
<span class="glossary">Rollover Credit:</span>
</strong>
Amount credited to a trader's account when the long currency of a currency pair has a higher yielding interest rate than the shorted currency.
</p>
<p>
<strong>
<span class="glossary">Rollover Debit:</span>
</strong>
Amount debited from a trader's account because of an overnight rollover, when the long currency of a currency pair has a lower yielding interest rate than the shorted currency.
</p>
<p>
<strong>
<span class="glossary">Rollover Rate:</span>
</strong>
Generally, the daily rollover interest rate is the amount a trader either pays or earns, depending on the currency pairs in question.
</p>
<p>
<strong>
<span class="glossary">Round Lot:</span>
</strong>
In most cases, 100,000 units of a currency.
</p>
<p>
<strong>
<span class="glossary">Round Trip:</span>
</strong>
The buying and selling of a currency pair and having the profit or loss applied to one's account currency.
</p>
<p>
<a href="#top">Back to Top</a>
<a name="S"></a>
</p>
<p> </p>
<h3>S</h3>
<p>
<strong>
<span class="glossary">Selling Short:</span>
</strong>
Selling a currency pair that involves being short the base currency and long the quote currency, with the intent of buying the currency pair at a later time when prices are lower in order to make a profit.
</p>
<p>
<strong>
<span class="glossary">Settlement:</span>
</strong>
The physical delivery of currencies made when a contract matures. In Forex, it is usually two days after the trade. In practice, traders don't take delivery, but profits and losses are applied directly to their account balance.
</p>
<p>
<strong>
<span class="glossary">Settlement Date:</span>
</strong>
In Forex, the date when physical delivery must take place. For most currency pairs it is two days after the trade date. However, the USD/CAD currency pair settles one day after its trade date.
</p>
<p>
<strong>
<span class="glossary">Short Covering:</span>
</strong>
Buying the exact same units of a currency pair to offset an earlier short sale of the same currency pair.
</p>
<p>
<strong>
<span class="glossary">Short Position:</span>
</strong>
In foreign exchange, when a currency pair is sold, the position is said to be short. It is understood that the primary currency in the pair is 'short', and the secondary currency is 'long'.
</p>
<p>
<strong>
<span class="glossary">Sidelined:</span>
</strong>
When there is above ordinary interest in a currency pair, other major currency pairs that are thinly traded as a result of this are considered sidelined.
</p>
<p>
<strong>
<span class="glossary">Spot:</span>
</strong>
Buying and selling Forex with the current date's price for valuation, but where settlement usually takes place in two days. Trades on FXTrade are settled immediately.
</p>
<p>
<strong>
<span class="glossary">Spot Market:</span>
</strong>
Market where people buy and sell actual financial instruments (currencies) for two-day delivery.
</p>
<p>
<strong>
<span class="glossary">Spot Price:</span>
</strong>
The current market price of a currency traded in the spot market.
</p>
<p>
<strong>
<span class="glossary">Spread:</span>
</strong>
The value difference between the bid and ask price of a currency pair.
</p>
<p>
<strong>
<span class="glossary">Stable Market:</span>
</strong>
A market that can accommodate huge volumes of buying and selling without large moves. For example, the trading of the EUR/USD pair.
</p>
<p>
<strong>
<span class="glossary">Sterilisation:</span>
</strong>
The process by which central banks offset intervention in the Forex market by activities in the domestic money market.
</p>
<p>
<strong>
<span class="glossary">Sterling:</span>
</strong>
Another name for the British Pound (GBP).
</p>
<p>
<strong>
<span class="glossary">Stop Order:</span>
</strong>
An order to buy or to sell a currency when the currency's price reaches or passes a specified level.
</p>
<p>
<strong>
<span class="glossary">Stop-buy:</span>
</strong>
A buy order for a currency price that is above the current market, or current price. It becomes a market order when the specified price is reached. Stop-buys are used by traders to establish positions in markets that they perceive to be rising in value.
</p>
<p>
<strong>
<span class="glossary">Swissy:</span>
</strong>
Trader's nickname for the Swiss Franc.
</p>
<p>
<a href="#top">Back to Top</a>
<a name="T"></a>
</p>
<p> </p>
<h3>T</h3>
<p>
<strong>
<span class="glossary">Take Profit Order:</span>
</strong>
A customer's instructions to buy or sell a currency pair which, when executed, will result in the reduction in the size of the existing position and show a profit on said position.
</p>
<p>
<strong>
<span class="glossary">Take Profits:</span>
</strong>
A limit order that is placed above the market with a long position or below the market with a short position. When the market reaches the limit price, the position is closed thereby locking in a profit
</p>
<p>
<strong>
<span class="glossary">Technical Analysis:</span>
</strong>
An effort to forecast prices by analysing market data, i.e. historical price trends and averages, volumes, open interest, etc.
</p>
<p>
<strong>
<span class="glossary">Technical Correlation:</span>
</strong>
A price adjustment based on technical factors like resistance and support levels, as well as overbought and oversold levels, instead of market sentiment.
</p>
<p>
<strong>
<span class="glossary">Technical Indicators:</span>
</strong>
Short-term trends that technical analysts use to predict future price movements of securities and/or commodities. Also called technicals, technicalities.
</p>
<p>
<strong>
<span class="glossary">Tick:</span>
</strong>
The smallest possible change in a price, either up or down. Also known as a pip.
</p>
<p>
<strong>
<span class="glossary">Ticker:</span>
</strong>
Streaming display of the current or recent historical price of a currency pair.
</p>
<p>
<strong>
<span class="glossary">Tradeable Amount:</span>
</strong>
The smallest transaction size allowed. For many brokers the tradeable amount is the round lot, which is usually 100,000 units of a particular currency. With FXTrade, it is 1 unit.
</p>
<p>
<strong>
<span class="glossary">Trading Model:</span>
</strong>
A sophisticated program that provides you with expert buy/sell recommendations for trading currencies on the foreign exchange markets. A Trading Model, based on its evaluation of historical analyses, forecasts, and your trading profile, makes recommendations about currency positions by anticipating fluctuations in the foreign exchange markets and capitalising on these movements.
</p>
<p>
<strong>
<span class="glossary">Trading Platforms:</span>
</strong>
A software application used for trading Forex, usually over the Internet.
</p>
<p>
<strong>
<span class="glossary">Transaction:</span>
</strong>
Buying or selling a currency pair.
</p>
<p>
<strong>
<span class="glossary">Transaction Cost:</span>
</strong>
The cost involved in buying or selling a currency pair. Some consider the transaction cost to be the actual value of the contract, while others feel it is the price of facilitating the trade, such as commissions and spreads.
</p>
<p>
<strong>
<span class="glossary">Trend:</span>
</strong>
The current direction of the market, whether up or down or sideways (which is sometimes referred to as non-trending or trading market).
</p>
<p>
